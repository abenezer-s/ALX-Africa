
#Print the results of each operation in the format: [operation] of [number1] and [number2] is [result].
number1 = 10
number2 = 5
#sum
result = number1 + number2
print("sum of {} and {} is {}".format(number1, number2, result))
#subtract
result = number1 - number2
print("subtraction of {} and {} is {}".format(number1, number2, result))
#multiply
result = number1 * number2
print("multiplication of {} and {} is {}".format(number1, number2, result))

